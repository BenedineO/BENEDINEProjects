# Creating an Age Group Identifier program using python.

# Requesting for user's age
age = float(input("Enter your age: "))

# Creating a conditional statement to capture all age categories.
if age < 13:      # For ages between 0 and 13 years
    print("You are a child")
elif 13 <= age <= 19:  # For ages between 13 and 19 years
    print('You are a teenager')
else:
    print("You are an adult")   # For ages above 19 years

